function findLocationInCityShow(){
    event.preventDefault();
    document.getElementById("localPageZoomCar").style.display="none"
    document.getElementById("searchLocationBox").style.display="block"
}
function findLocationInCityHide(){
    event.preventDefault();
    document.getElementById("localPageZoomCar").style.display="block"
    document.getElementById("searchLocationBox").style.display="none"
}